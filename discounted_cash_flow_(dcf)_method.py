import numpy as np

# Define the bond's parameters
face_value = 1000  # Face value of the bond
coupon_rate = 0.05  # Annual coupon rate
coupon_payment = face_value * coupon_rate / 2  # Semi-annual coupon payment
years_to_maturity = 5  # Years to maturity
discount_rate = 0.06  # Required rate of return of the investor

# Calculate the present value of the bond's cash flows
present_value = 0
for t in range(1, years_to_maturity*2+1):
    if t == years_to_maturity*2:
        coupon_payment += face_value  # Add the face value to the last coupon payment
    present_value += coupon_payment / (1 + discount_rate/2)**t
    
# Print the bond's price
print("The bond's price is: ${:.2f}".format(present_value))

